numeros = [2, 4, 6, 8, 10]  # cria uma lista com 5 números inteiros

soma = 0  # declara uma variável para armazenar a soma dos valores da lista

for num in numeros:  # percorre todos os elementos da lista
    soma += num  # soma o valor do elemento atual na variável 'soma'

print("A soma dos números é:", soma)  # imprime o resultado da soma na tela