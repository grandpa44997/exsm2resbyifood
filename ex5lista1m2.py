# Criando uma lista com 6 elementos
minha_lista = [10, 20, 30, 40, 50, 60]

# Imprimindo os três primeiros elementos
print(minha_lista[:3])  # do zero ao terceiro elemento sem contar o terceiro elemento

# Imprimindo os três últimos elementos
print(minha_lista[-3:])  # do final ao penultimo elemento

# Imprimindo uma sub-lista com o segundo e o terceiro elementos
print(minha_lista[1:3])  # Saída: [20, 30]